    private void descripcionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_descripcionMouseClicked
        // TODO add your handling code here:
        if(clave.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Proporcione la clave del producto");
        }
        else{
           int c;
        verifica=0;
        uni=0;
        c=Integer.parseInt(clave.getText().toString());        
         
         try// Método para mostrar la descripcion del producto
         {
             Conexion2 miconexion=new Conexion2();//se conecta 
             
             String cons="select *from tablacatalogoproductos";             
             ResultSet consulta=miconexion.consulta(cons); //se envia la consulta
             
             if(consulta.next())//pegunta si hay datos en la tabla
             {
                    do{ 
                        if(c==Integer.parseInt(consulta.getString(1))){
                          descripcion.setText(consulta.getString(2));
                          uni=Integer.parseInt(consulta.getString(3));
                          verifica=1;
                        };
                  }while(consulta.next());
                 if(verifica!=1){
                     JOptionPane.showMessageDialog(null,"La clave del producto es incorrecta");
                     clave.setText(""); 
                     descripcion.setText("");
                     unidad.setText("");
                     cantidad.setText("0");
                     precio.setText("0.0");
                     totalmov.setText("0.0");                    
                 }
             }
             
             
         }         
         catch(Exception ex)
         {
             System.out.print(ex);
         }
         
         try// Método para mostrar la unidad del producto
         {
             Conexion2 miconexion=new Conexion2();//se conecta 
             
             String cons="select *from tablacatalogounidades";             
             ResultSet consulta=miconexion.consulta(cons); //se envia la consulta
             
             if(consulta.next())//pegunta si hay datos en la tabla
             {
                    do{ 
                        if(uni==Integer.parseInt(consulta.getString(1))){
                          unidad.setText(consulta.getString(2));                          
                        }
                  }while(consulta.next());
             }       
             
         }         
         catch(Exception ex)
         {
             System.out.print(ex);
         } 
        }
    }//GEN-LAST:event_descripcionMouseClicked

